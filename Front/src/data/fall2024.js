// 블록을 파싱해서 players 맵에 병합
function ingestBlock(playersMap, position, category, lines) {
  const schema = SCHEMA[`${position}|${category}`];
  lines
    .map((l) => l.trim())
    .filter((l) => l && !l.startsWith('//'))
    .forEach((line) => {
      const parsed = parseLine(line);
      if (!parsed) return;
      const { name, team, stats } = parsed;
      const k = keyOf(position, team, name);
      if (!playersMap.has(k)) {
        playersMap.set(k, { position, team, division: DIVISION, name });
      }
      const row = playersMap.get(k);
      schema.forEach((field, idx) => {
        row[field] = stats[idx] ?? row[field] ?? (isPairToken(field) ? '0-0' : 0);
      });
    });
}


