// 숫자/쌍(예: "4-7") 토큰 식별
const isNumberToken = (t) => /^-?\d+(\.\d+)?$/.test(t);
const isPairToken = (t) => /^\d+-\d+$/.test(t);
const isStatToken = (t) => isNumberToken(t) || isPairToken(t) || /%$/.test(t);

// 값 파싱: "3-5"같은 쌍은 문자열 그대로, 나머지는 숫자
const parseVal = (t) => (isPairToken(t) ? t : parseFloat(t));

// 라인 파싱: "<등번호>번 <팀명 ...> <값...>"
function parseLine(line) {
  const tokens = line.trim().split(/\s+/);
  if (!tokens.length) return null;
  // 등번호 (예: "15번")
  const name = tokens[0]; // 화면에 name으로 노출
  let i = 1;
  const teamTokens = [];
  while (i < tokens.length && !isStatToken(tokens[i])) {
    teamTokens.push(tokens[i]);
    i += 1;
  }
  const team = teamTokens.join(' ');
  const statTokens = tokens.slice(i);
  return { name, team, stats: statTokens.map(parseVal) };
}

